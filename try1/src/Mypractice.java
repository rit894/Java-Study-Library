// JAVA VARIABLES
// there are five types of variables in java such as:
/* int, float, char, boolean, String */
class Variables {
      public static void main(String[] args) {
        // Variable declaration and initialization
        int myNum = 5;               // Integer (whole number)
        float myFloatNum = 5.99f;   // Floating point number
        char myLetter = 'D';        // Character
        boolean myBool = true;      // Boolean
        String myText = "Hello";    // String
        double myDoubleNum  = 12345.6789; // Double precision floating point number 
        // Print variables
        System.out.println("Integer: " + myNum);
        System.out.println("Float: " + myFloatNum);
        System.out.println("Character: " + myLetter);
        System.out.println("Boolean: " + myBool);
        System.out.println("String: " + myText);
        System.out.println("Double: " + myDoubleNum);
        int myNum1 = 10;
        double MYdouble = 12345.678;
        System.out.println(myNum1);
        System.out.println(MYdouble);
        float myfloat = 5.57f;
        System.out.println(myfloat);
        boolean myboolean = false;
        System.out.println(myboolean); 
        String myString = "Java Programming";
        System.out.println(myString);
    }
}
